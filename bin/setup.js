#!/usr/bin/env node

import { createInterface } from "readline";
import { writeFileSync, existsSync } from "fs";
import { resolve, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const envPath = resolve(__dirname, "..", ".env");

const rl = createInterface({
  input: process.stdin,
  output: process.stdout,
});

function ask(question, defaultValue) {
  return new Promise((resolve) => {
    const prompt = defaultValue
      ? `${question} (${defaultValue}): `
      : `${question}: `;
    rl.question(prompt, (answer) => {
      resolve(answer.trim() || defaultValue || "");
    });
  });
}

async function main() {
  console.log("");
  console.log("===========================================");
  console.log("  Motion Calendar MCP - Setup Wizard");
  console.log("===========================================");
  console.log("");

  if (existsSync(envPath)) {
    const overwrite = await ask(
      "A .env file already exists. Overwrite? (y/N)",
      "N"
    );
    if (overwrite.toLowerCase() !== "y") {
      console.log("\nSetup cancelled. Existing .env file preserved.");
      rl.close();
      return;
    }
    console.log("");
  }

  console.log("You'll need the following credentials.");
  console.log("See the README for detailed instructions on where to find them.");
  console.log("");

  const motionApiKey = await ask("Motion API key (from Motion Settings > API)");
  const firebaseApiKey = await ask("Firebase API key (from browser DevTools)");
  const firebaseRefreshToken = await ask(
    "Firebase refresh token (from browser DevTools)"
  );
  const motionUserId = await ask("Motion user ID (from browser DevTools)");
  const timezone = await ask("Timezone", "America/New_York");

  const envContent = `# Motion Calendar MCP Configuration
# Generated by setup wizard

MOTION_API_KEY=${motionApiKey}
FIREBASE_API_KEY=${firebaseApiKey}
FIREBASE_REFRESH_TOKEN=${firebaseRefreshToken}
MOTION_USER_ID=${motionUserId}
MOTION_TIMEZONE=${timezone}
`;

  writeFileSync(envPath, envContent, { mode: 0o600 });

  console.log("");
  console.log("===========================================");
  console.log("  Setup complete!");
  console.log("===========================================");
  console.log("");
  console.log(`  .env written to: ${envPath}`);
  console.log("");
  console.log("  Next steps:");
  console.log("  1. Add the MCP server to your Claude config:");
  console.log("");
  console.log('     claude mcp add motion-calendar -- npx motion-calendar-mcp');
  console.log("");
  console.log("  2. Or run directly:");
  console.log("");
  console.log("     npm start");
  console.log("");

  rl.close();
}

main().catch((err) => {
  console.error("Setup failed:", err.message);
  rl.close();
  process.exit(1);
});
